package main

import "fmt"

func main() {
	//位运算
	//1101 13
	//11 3
	fmt.Printf("13的二进制%b\n", 13)
	fmt.Printf("3的二进制%b\n", 3)

	// Println输出十进制数
	//& （两个对应的二进制位都有1才为1）
	fmt.Println(13 & 3) //0001->1
	//| (两个对应的二进制位有一个为1，即为1)
	fmt.Println(13 | 3) //1111->15
	//^ (两个对应的二进制位不一样，为1)
	fmt.Println(13 ^ 3) //1110->14
	//<< (将某个数的二进制数左移多少位)
	fmt.Println(3 << 10) // 把3的二进制数左移十位->110000000000
	//>> (将某个数的二进制数右移多少位)
	fmt.Println(3 >> 1)  //1，溢出的舍掉
	fmt.Println(3 >> 10) //0

}
